var pl_check_time_run = 0;
var pl_time_run;
function AJ_seach_sp(obj, cls, lienhe, text_tontai_ss){
	var aj_text  = $(obj).val();
	if(aj_text.length > 1)
		{
			pl_check_time_run = 1;
			clearInterval(pl_time_run);
			pl_time_run = setInterval(function() {
				//tim kiem
				if(pl_check_time_run == 1)
					{
						var action = "search_sp";
						$.ajax({
				            type: "POST",
				            url: "/search_sosanh",
				            data: {"action": action, "key" : aj_text, "cls": cls, "lienhe": lienhe, "text_tontai_ss": text_tontai_ss},
				            success: function(response){
				            	if(response != '')
				            		{
				            			$(".aj_search_tk" + cls).html(response);
				            			$(".aj_search_tk" + cls).show();
				            		}
				            	else
									{
										$(".aj_search_tk" + cls).html("");
										$(".aj_search_tk" + cls).hide();
									}
				            }
				        });
						pl_check_time_run = 0;
					}
				clearInterval(pl_time_run);
				//
			}, 600);
		}
	else
		{
			$(".aj_search_tk" + cls).html("");
			$(".aj_search_tk" + cls).hide();
		}
}

function SOSANH_add_sp(id, action, cls, lienhe, text_tontai_ss)
	{
		$.ajax({
            type: "POST",
            url: "/search_sosanh",
            data: {"action": action, "key" : id, "cls": cls, "lienhe": lienhe},
            success: function(response){
            	// console.log(response)
				response = JSON.parse(response);
				if(response.ma_loi == '-1') 		alert(text_tontai_ss);
				else if(response.num_row != 0)
					{
						$(".js-sosanh").html("("+response.so_sp+")");
						$(".dv-sosanh-box-ip-" + cls).hide();
						$(".dv-cont-box-ss-" + cls).html(response.html);
						$(".dv-cont-box-ss-" + cls).show();
						$(".aj_search_tk" + cls).html('');
						$(".aj_search_tk" + cls).hide();
					$(".ip_sosanh_h" + cls).val('');
					$(".dv-box-sosanh").show();
					}
				SOSANH_auto_height();
            }
        });
	}

function SOSANH_remove_sp(id, action, cls)
{
	$.ajax({
		type: "POST",
		url: "/search_sosanh",
		data: {"action": action, "key" : id, "cls": cls},
		success: function(response){
			// console.log(response);
			if(response != 0)
			{
				response = JSON.parse(response);
				$(".js-sosanh").html("("+response.so_sp+")");
				$(".dv-sosanh-box-ip-" + cls).show();
				$(".dv-cont-box-ss-" + cls).html("");
				$(".dv-cont-box-ss-" + cls).hide();
				if(response.so_sp == 0){
					XOA_all_ss();
				}
			}
		}
	});
}

function SOSANH_sp (id, action, lienhe, text_tontai_ss, text_limit_3_ss) {
	var div_1 = $(".dv-cont-box-ss-1").html();
	var div_2 = $(".dv-cont-box-ss-2").html();
	var div_3 = $(".dv-cont-box-ss-3").html();
	var cls   = 0;
	if(div_1.trim()  == '')
		{
			cls   = 1;
		}
	else if(div_2.trim()  == '')
		{
			cls   = 2;
		}
	else if(div_3.trim()  == '')
		{
			cls   = 3;
		}
	$.ajax({
        type: "POST",
        url: "/add_sosanh",
        data: {"action": action, "id" : id, "cls" : cls, "lienhe" : lienhe},
        success: function(response){
        	// console.log(response)
        	if(response != '')
			{
				response = JSON.parse(response);
				if(response.ma_loi == '-1')
				{
					alert(text_tontai_ss);
					$(".dv-sosanh-min").show();
				}
				else if(response.ma_loi == '-2')
				{
					alert(text_limit_3_ss);
					$(".dv-sosanh-min").show();
				}
				else if(response.so_sp  == 0)
				{
					XOA_all_ss();
				}
				else if(response.so_sp  != 0)
				{
					// console.log(response);
					$(".dv-box-sosanh").show();
					$(".js-sosanh").html("("+response.so_sp+")");

					$(".dv-sosanh-min").show();
					//kiem tra ai con trong
					$(".dv-cont-box-ss-" + response.cls).html(response.html);
					$(".dv-cont-box-ss-" + response.cls).show();
					$(".dv-sosanh-box-ip-" + response.cls).hide();
					//end
				}
			}
        }
    });
}
